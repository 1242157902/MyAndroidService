package cn.ncut.pushdomain;

public class AutoPushLog {
	private String id;
	private String queid;
	private String startt;
	private String endt;
	private String imeis;
	private String pusht;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getQueid() {
		return queid;
	}
	public void setQueid(String queid) {
		this.queid = queid;
	}
	public String getStartt() {
		return startt;
	}
	public void setStartt(String startt) {
		this.startt = startt;
	}
	public String getEndt() {
		return endt;
	}
	public void setEndt(String endt) {
		this.endt = endt;
	}
	public String getImeis() {
		return imeis;
	}
	public void setImeis(String imeis) {
		this.imeis = imeis;
	}
	public String getPusht() {
		return pusht;
	}
	public void setPusht(String pusht) {
		this.pusht = pusht;
	}
}
